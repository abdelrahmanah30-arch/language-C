#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	c;

	if (argc == 2)
	{
		i = 0;
		while (argv[1][i] != '\0')
		{
			c = argv[1][i];
			if (c >= 'A' && c <= 'Z')
				c += 32;
			else if (c >= 'a' && c <= 'z')
				c -= 32;
			write (1, &c, 1);
			i++;
		}
	}
	write (1, "\n", 2);
}
